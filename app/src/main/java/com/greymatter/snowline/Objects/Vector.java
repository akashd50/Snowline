package com.greymatter.snowline.Objects;

public class Vector {
    public int x,y,z;
    public Vector(){}
    public Vector(int x,int y){
        this.x = x; this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getZ() {
        return z;
    }

    public void setZ(int z) {
        this.z = z;
    }
}
